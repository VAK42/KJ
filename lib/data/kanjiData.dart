final kanjiDataProvider = FutureProvider<Map<String, List<KanjiModel>>>((ref) async {
  final jsonStr = await rootBundle.loadString('assets/kanjiData.json');
  final jsonMap = json.decode(jsonStr) as Map<String, dynamic>;
  final result = <String, List<KanjiModel>>{};
  for (final level in ['N5', 'N4', 'N3', 'N2', 'N1']) {
    final list = jsonMap[level] as List<dynamic>? ?? [];
    result[level] = list.map((e) => KanjiModel.fromJson(e as Map<String, dynamic>)).toList();
  }
  return result;
});
final kanjiByLevelProvider = Provider.family<AsyncValue<List<KanjiModel>>, String>((ref, level) {
  return ref.watch(kanjiDataProvider).whenData((data) => data[level] ?? []);
});
